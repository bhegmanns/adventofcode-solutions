package de.hegmanns.training.aoc2022.day07;

public class SimpleLeaf {

    private long size;
    private String name;

    public SimpleLeaf(long size, String name) {
        this.size = size;
        this.name = name;
    }

    public long getSize() {
        return size;
    }

    public String getName() {
        return name;
    }
}
